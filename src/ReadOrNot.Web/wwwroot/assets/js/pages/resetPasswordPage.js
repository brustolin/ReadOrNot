import { showMessage } from "../components/ui.js";

export function renderResetPasswordPage(app, context, route) {
  const query = route.query;
  app.innerHTML = `
    <section class="auth-card stack">
      <div>
        <h1 class="auth-title">Reset Password</h1>
        <p class="muted">Use the token generated by the API to set a new password.</p>
      </div>
      <div id="reset-message"></div>
      <form id="reset-form" class="stack">
        <div class="field">
          <label for="email">Email</label>
          <input id="email" name="email" type="email" required value="${query.email ?? ""}">
        </div>
        <div class="field">
          <label for="token">Reset token</label>
          <textarea id="token" name="token" required>${query.token ?? ""}</textarea>
        </div>
        <div class="field">
          <label for="newPassword">New password</label>
          <input id="newPassword" name="newPassword" type="password" required>
        </div>
        <div class="field">
          <label for="confirmPassword">Confirm password</label>
          <input id="confirmPassword" name="confirmPassword" type="password" required>
        </div>
        <button class="button" type="submit">Reset password</button>
      </form>
    </section>
  `;

  app.querySelector("#reset-form").addEventListener("submit", async (event) => {
    event.preventDefault();
    const form = new FormData(event.currentTarget);

    try {
      await context.apis.auth.resetPassword({
        email: form.get("email"),
        token: form.get("token"),
        newPassword: form.get("newPassword"),
        confirmPassword: form.get("confirmPassword")
      });

      showMessage(app.querySelector("#reset-message"), "Password reset complete. You can log in now.", "notice");
    } catch (error) {
      showMessage(app.querySelector("#reset-message"), error.message, "error");
    }
  });
}
